class RedeSocial():
    def __init__(self, nome, descricao):
      self.nome = nome
      self.descricao = descricao

    def setNome(self,nome):
        self.nome = nome
    def setDescricao(self,descricao):
        self.descricao = descricao

    def getNome(self):
        return self.nome
    def setDescricao(self):
        return self.descricao